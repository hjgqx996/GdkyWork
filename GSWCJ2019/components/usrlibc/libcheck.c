uint16_t chk_crc16_X25(const void * pbuf, uint32_t len)
{
  uint8_t i = 0;
  uint16_t crc = 0xFFFF;
  const uint8_t *pdat = pbuf;

  if (NULL == pdat)
    return 0;

  while(len)
  {
    crc ^= *pdat;

    for (i = 0; i < 8; ++i)
    {
      if (crc & 1)
        crc = (crc >> 1) ^ 0x8408; // 0x8408 = reverse 0x1021
      else
        crc = (crc >> 1);
    }

    len -= 1;
    pdat += 1;
  }
  return (crc ^ 0xFFFF); // crc^Xorout
}

